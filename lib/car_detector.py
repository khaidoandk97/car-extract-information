class CardDetector:
    def __init__(self):
        self.detector = Yolov8Detect(CAR_DETECTION_MODEL_PATH)

    def run(self, image, debug_dir=None):
        boxes, labels, scores = self.detector(image)
        cars_box = boxes[labels==2]
        cars_score = scores[labels==2]
        if debug_dir:
            debug_image = image.copy()
            for box, score in zip(cars_box, cars_score):
                cv2.rectangle(debug_image, box[:2], box[2:], (0, 255, 0), 2)
                cv2.putText(debug_image, "{:.2f}".format(score), (box[0], box[1] - 5), cv2.FONT_HERSHEY_SIMPLEX, 0.8, (51, 8, 194), 2)
                cv2.imwrite(os.path.join(debug_dir, "car_detection.png"), debug_image)
        return cars_box
    
