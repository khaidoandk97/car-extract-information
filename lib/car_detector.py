class Yolov8Detect:
    def __init__(self,
                 weight='',
                 device='cpu',
                 size=640,
                 conf_thres=0.6,
                 iou_thres=0.6,
                 max_det=50) -> None:
        self.size, self.conf_thres, self.iou_thres, self.max_det, self.device = size, conf_thres, iou_thres, max_det, device
        if device=='cpu':
            providers = ['CPUExecutionProvider']
        else:
            providers = ['CUDAExecutionProvider', 'CPUExecutionProvider']
            
        self.session = ort.InferenceSession(weight, providers=providers)
        self.outname = [i.name for i in self.session.get_outputs()]
        self.inname = self.session.get_inputs()[0].name
        
        
    def __call__(self, image):
        im = self._preprocess(image)
        outs = self.session.run(self.outname, {self.inname:im})
        boxes, labels, scores = self._processOutput(outs, im.shape[2:], image.shape)
        return boxes, labels, scores
    
    def _preprocess(self, image):
        # Preprocess
        im = image.copy()
        # Padded resize
        im = letterbox(im, self.size, stride=32, auto=False)[0]
        # Convert 
        im = im.transpose((2, 0, 1))[::-1] # HWC to CHW, BGR to RGB
        im = np.expand_dims(im, 0)
        im = np.ascontiguousarray(im)
        im = im.astype(np.float32)
        im /= 255
        return im
    
    def _processOutput(self, outs, shapeScale, shapeOrigin):

        pred = non_max_suppression(outs, self.conf_thres, self.iou_thres, None, False, False, () ,max_det=self.max_det)[0]
        boxes, labels, scores = np.array([]), np.array([]), np.array([])
        if len(pred):
            pred[:, :4] = scale_coords(shapeScale, pred[:, :4], shapeOrigin).round()  # xyxy format
            pred = pred.cpu().detach().numpy()
            boxes, labels, scores = pred[:,:4].astype(int), pred[:,5].astype(np.uint8) , pred[:,4]
            
        return boxes, labels, scores

